"""## 🚀 Cell 7 — Simulation runner (shared by Oort & FedAvg)"""

def run_experiment(
    dataset_name:    str   = 'mnist',
    num_clients:     int   = 50,
    alpha                  = 0.01,    # Dirichlet alpha or 'iid'
    num_rounds:      int   = 50,
    strategy_name:   str   = 'oort', # 'oort' or 'fedavg'
    straggler_ratio: float = 0.2,
    num_cpus:        float = 1.0,
    num_gpus:        float = 0.0,    # set >0 if Colab has GPU
):
    """
    Run a full FL simulation with either Oort or FedAvg strategy.
    Returns (history_df, strategy_object, model_size_mb).
    """
    print(f'\n{'='*65}')
    print(f'  Strategy={strategy_name.upper()} | Dataset={dataset_name.upper()} | '
          f'Clients={num_clients} | α={alpha} | Rounds={num_rounds}')
    print(f'{'='*65}')

    # ── Load & partition data ────────────────────────────────────────────
    train_ds, test_ds = load_dataset(dataset_name)
    client_indices    = dirichlet_partition(train_ds, num_clients, alpha)
    client_speeds     = simulate_straggler_speeds(num_clients, straggler_ratio)

    model_mb = model_size_mb(get_model(dataset_name))
    k_per_round = max(1, int(num_clients * 0.5))  # 50% per round

    # ── Global test evaluation fn (server-side, every round) ────────────
    def get_evaluate_fn():
        def evaluate_fn(server_round, params, config):
            model = get_model(dataset_name).to(DEVICE)
            set_params(model, params)
            model.eval()
            loader    = DataLoader(test_ds, batch_size=256, shuffle=False)
            criterion = nn.CrossEntropyLoss()
            loss, correct, total = 0.0, 0, 0
            with torch.no_grad():
                for x, y in loader:
                    x, y = x.to(DEVICE), y.to(DEVICE)
                    out   = model(x)
                    loss += criterion(out, y).item() * len(y)
                    correct += (out.argmax(1) == y).sum().item()
                    total   += len(y)
            acc  = correct / max(total, 1)
            return float(loss / max(total, 1)), {'accuracy': float(acc)}
        return evaluate_fn

    # ── Metrics aggregation fns ──────────────────────────────────────────
    def fit_metrics_agg(metrics):
        losses = [n * m.get('train_loss', 0) for n, m in metrics]
        total  = sum(n for n, _ in metrics)
        return {'avg_train_loss': sum(losses) / max(total, 1)}

    def eval_metrics_agg(metrics):
        accs  = [n * m.get('accuracy', 0) for n, m in metrics]
        total = sum(n for n, _ in metrics)
        return {'accuracy': sum(accs) / max(total, 1)}

    # ── Strategy ────────────────────────────────────────────────────────
    common_kwargs = dict(
        fraction_fit=0.5,
        fraction_evaluate=0.5,
        min_fit_clients=k_per_round,
        min_evaluate_clients=max(1, k_per_round // 2),
        min_available_clients=num_clients,
        evaluate_fn=get_evaluate_fn(),
        fit_metrics_aggregation_fn=fit_metrics_agg,
        evaluate_metrics_aggregation_fn=eval_metrics_agg,
    )

    if strategy_name.lower() == 'oort':
        strategy = OortStrategy(
            num_clients_per_round=k_per_round,
            exploration_factor=0.2,
            pacer_step=5,
            pacer_delta=0.05,
            decay=0.9,
            **common_kwargs,
        )
    else:
        strategy = FedAvg(**common_kwargs)

    # ── Client factory fn ────────────────────────────────────────────────
    def client_fn(cid: str):
        idx   = int(cid)
        speed = client_speeds[idx]
        return OortClient(
            cid=idx,
            train_indices=client_indices[idx],
            train_dataset=train_ds,
            test_dataset=test_ds,
            dataset_name=dataset_name,
            compute_speed=speed,
        )

    # ── Run simulation ───────────────────────────────────────────────────
    history = fl.simulation.start_simulation(
        client_fn=client_fn,
        num_clients=num_clients,
        config=fl.server.ServerConfig(num_rounds=num_rounds),
        strategy=strategy,
        client_resources={'num_cpus': num_cpus, 'num_gpus': num_gpus},
        ray_init_args={'ignore_reinit_error': True, 'include_dashboard': False},
    )

    # ── Build results DataFrame ──────────────────────────────────────────
    rounds     = [r for r, _ in history.metrics_centralized.get('accuracy', [])]
    accs       = [float(v) for _, v in history.metrics_centralized.get('accuracy', [])]
    losses_cen = [float(v) for _, v in history.losses_centralized]

    # Pad if lengths differ (round 0 may have extra entry)
    min_len = min(len(rounds), len(accs), len(losses_cen))

    comm_cost_mb = model_mb * k_per_round * 2  # upload + download per round
    df = pd.DataFrame({
        'round':        rounds[:min_len],
        'accuracy':     accs[:min_len],
        'loss':         losses_cen[:min_len],
        'strategy':     strategy_name.upper(),
        'dataset':      dataset_name.upper(),
        'num_clients':  num_clients,
        'alpha':        str(alpha),
        'comm_cost_mb': [comm_cost_mb * (i+1) for i in range(min_len)],
    })

    # Convergence round (first round ≥ 80% accuracy)
    conv_rounds = df[df['accuracy'] >= 0.80]
    conv_round  = int(conv_rounds['round'].iloc[0]) if len(conv_rounds) > 0 else -1
    final_acc   = float(df['accuracy'].iloc[-1]) if len(df) > 0 else 0.0
    total_comm  = float(df['comm_cost_mb'].iloc[-1]) if len(df) > 0 else 0.0

    print(f'\n  ✅ Done! Final accuracy: {final_acc:.4f} | '
          f'Convergence round (≥80%): {conv_round} | '
          f'Total comm: {total_comm:.1f} MB')

    return df, strategy, model_mb

