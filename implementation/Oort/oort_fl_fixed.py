#  DIRICHLET PARTITIONING
# ─────────────────────────────────────────────────────────────────────────
def dirichlet_partition(dataset, num_clients: int, alpha,
                        seed: int = 42, min_samples: int = 10) -> List[List[int]]:
    """
    Partition dataset indices using Dirichlet(alpha).
    alpha='iid' or None → uniform IID split.
    Small alpha (0.01) → extreme non-IID.
    min_samples guarantees every client has at least this many samples
    (prevents empty-partition crashes with extreme alpha + many clients).
    """
    np.random.seed(seed)
    targets = np.array([dataset[i][1] for i in range(len(dataset))])
    num_classes = len(np.unique(targets))

    # IID fallback
    if alpha is None or str(alpha).lower() == 'iid':
        idx = np.random.permutation(len(dataset))
        return [s.tolist() for s in np.array_split(idx, num_clients)]

    # Build per-class index lists
    class_idx = {c: [] for c in range(num_classes)}
    for i, lbl in enumerate(targets):
        class_idx[lbl].append(i)
    for c in class_idx:
        np.random.shuffle(class_idx[c])

    client_idx: List[List[int]] = [[] for _ in range(num_clients)]
    for c in range(num_classes):
        props = np.random.dirichlet(np.repeat(alpha, num_clients))
        props = (np.cumsum(props) * len(class_idx[c])).astype(int)[:-1]
        for j, split in enumerate(np.split(class_idx[c], props)):
            client_idx[j].extend(split.tolist())

    for j in range(num_clients):
        np.random.shuffle(client_idx[j])

    # ✅ FIX 1: Guarantee every client has at least min_samples
    # Prevents DataLoader crash: "num_samples should be a positive integer, got 0"
    all_indices = [i for sub in client_idx for i in sub]
    for j in range(num_clients):
        if len(client_idx[j]) < min_samples:
            needed = min_samples - len(client_idx[j])
            extras = np.random.choice(all_indices, size=needed, replace=True).tolist()
            client_idx[j].extend(extras)

    return client_idx


def simulate_straggler_speeds(num_clients: int, straggler_ratio: float = 0.2,
                               seed: int = 42) -> List[float]:
    """Assign simulated compute speeds (Cat-7 system heterogeneity)."""
    rng = np.random.RandomState(seed)
    speeds = []
    for _ in range(num_clients):
        if rng.random() < straggler_ratio:
            speeds.append(float(rng.uniform(0.1, 0.4)))  # straggler
        else:
            speeds.append(float(rng.uniform(0.7, 1.0)))  # normal
    return speeds

