"""## ⚙️ Cell 6 — Oort Strategy (core algorithm)"""

class OortStrategy(FedAvg):
    """
    Oort guided participant selection (Lai et al., OSDI 2021).

    Three core mechanisms:
      1. Statistical utility  = loss × √(num_samples)   [Eq.1 in paper]
      2. System utility       = penalty for slow clients
      3. Exploration bonus    = UCB-style boost for under-selected clients

    Selection: score all clients → pick top K×(1+ε) pool → softmax sample K.
    Pacer: every `pacer_step` rounds, relax the system deadline to admit
           slower-but-useful clients.
    """

    def __init__(self, num_clients_per_round: int = 10,
                 exploration_factor: float = 0.2,
                 pacer_step: int = 5, pacer_delta: float = 0.05,
                 decay: float = 0.9, clip_loss: float = 5.0,
                 **fedavg_kwargs):
        super().__init__(**fedavg_kwargs)
        self.K            = num_clients_per_round
        self.epsilon      = exploration_factor
        self.pacer_step   = pacer_step
        self.pacer_delta  = pacer_delta
        self.decay        = decay
        self.clip_loss    = clip_loss

        # Per-client state
        self._stat:     Dict[str, float] = {}   # statistical utility
        self._sys:      Dict[str, float] = {}   # system utility
        self._last_sel: Dict[str, int]   = {}   # last round selected
        self._n_sel:    Dict[str, int]   = {}   # times selected

        self._deadline    = 1.0   # system-speed threshold (Pacer)
        self._cur_round   = 0

        # For results tracking
        self.history: List[Dict] = []

    # ── Utility functions ────────────────────────────────────────────────
    def _stat_util(self, loss, n_samples):
        return min(loss, self.clip_loss) * math.sqrt(n_samples)

    def _sys_util(self, speed):
        return min(self._deadline / max(speed, 1e-6), 1.0)

    def _explore_bonus(self, cid, rnd):
        n = self._n_sel.get(cid, 0)
        return 1.0 if n == 0 else math.sqrt(math.log(rnd + 1) / n)

    def _temporal_decay(self, cid, rnd):
        return self.decay ** (rnd - self._last_sel.get(cid, 0))

    def _score(self, cid, rnd):
        stat  = self._stat.get(cid, 1.0)
        sys   = self._sys.get(cid, 1.0)
        expl  = self._explore_bonus(cid, rnd)
        td    = self._temporal_decay(cid, rnd)
        return stat * sys * expl * td

    # ── Flower hooks ─────────────────────────────────────────────────────
    def configure_fit(self, server_round, parameters, client_manager):
        self._cur_round = server_round

        # Pacer: relax deadline every pacer_step rounds
        if server_round > 0 and server_round % self.pacer_step == 0:
            self._deadline = min(1.0, self._deadline + self.pacer_delta)

        config  = {'local_epochs': 5, 'batch_size': 32, 'lr': 0.01,
                   'server_round': server_round}
        fit_ins = FitIns(parameters, config)

        all_clients = list(client_manager.all().values())
        n_available = len(all_clients)
        k = min(self.K, n_available)

        # Warm-up: random selection until we have utility scores
        if len(self._stat) < max(1, k):
            chosen = np.random.choice(all_clients, size=k, replace=False).tolist()
            return [(c, fit_ins) for c in chosen]

        # Score all clients
        scored = sorted(
            [(c, self._score(c.cid, server_round)) for c in all_clients],
            key=lambda x: x[1], reverse=True
        )

        # Exploration-exploitation pool
        pool_size  = min(int(k * (1 + self.epsilon)), n_available)
        pool       = [c for c, _ in scored[:pool_size]]
        pool_scores= np.array([s for _, s in scored[:pool_size]], dtype=float)

        # Softmax-weighted sampling from pool
        pool_scores -= pool_scores.max()          # numerical stability
        weights = np.exp(pool_scores)
        weights /= weights.sum()
        chosen_idx = np.random.choice(len(pool), size=k, replace=False, p=weights)
        chosen = [pool[i] for i in chosen_idx]
        return [(c, fit_ins) for c in chosen]

    def aggregate_fit(self, server_round, results, failures):
        if not results:
            return None, {}

        # Update per-client utility scores
        for client, fit_res in results:
            cid     = client.cid
            m       = fit_res.metrics or {}
            loss    = float(m.get('train_loss',    1.0))
            samples = int(  m.get('train_samples', 1))
            speed   = float(m.get('compute_speed', 1.0))

            self._stat[cid]     = self._stat_util(loss, samples)
            self._sys[cid]      = self._sys_util(speed)
            self._last_sel[cid] = server_round
            self._n_sel[cid]    = self._n_sel.get(cid, 0) + 1

        # FedAvg weighted aggregation
        weight_res = [(parameters_to_ndarrays(r.parameters), r.num_examples)
                      for _, r in results]
        agg_params = ndarrays_to_parameters(aggregate(weight_res))

        # Round-level metrics
        avg_loss = np.mean([float(r.metrics.get('train_loss', 0)) for _, r in results])
        avg_spd  = np.mean([float(r.metrics.get('compute_speed', 1)) for _, r in results])
        strag    = np.mean([1.0 if float(r.metrics.get('compute_speed',1)) < 0.5
                            else 0.0 for _, r in results])

        self.history.append({
            'round':           server_round,
            'num_selected':    len(results),
            'avg_train_loss':  float(avg_loss),
            'avg_speed':       float(avg_spd),
            'straggler_ratio': float(strag),
            'pacer_deadline':  float(self._deadline),
        })

        return agg_params, {'avg_train_loss': float(avg_loss),
                            'straggler_ratio': float(strag)}

    def aggregate_evaluate(self, server_round, results, failures):
        if not results:
            return None, {}
        loss = weighted_loss_avg([(r.num_examples, r.loss) for _, r in results])
        acc  = float(np.mean([float(r.metrics.get('accuracy', 0)) for _, r in results]))
        return loss, {'accuracy': acc}

