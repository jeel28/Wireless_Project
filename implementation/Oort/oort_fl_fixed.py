class MnistCNN(nn.Module):
    def __init__(self, num_classes=10):
        super().__init__()
        self.conv1   = nn.Conv2d(1, 32, 3, padding=1)
        self.conv2   = nn.Conv2d(32, 64, 3, padding=1)
        self.pool    = nn.MaxPool2d(2, 2)
        # ✅ FIX 2: Removed nn.Dropout(0.25) — CUDA dropout triggers SIGFPE
        # (Floating Point Exception) when batch sizes are small/edge-case,
        # killing the Ray worker actor and crashing the entire simulation.
        self.fc1     = nn.Linear(64 * 7 * 7, 128)
        self.fc2     = nn.Linear(128, num_classes)

    def forward(self, x):
        x = self.pool(F.relu(self.conv1(x)))   # 14×14
        x = self.pool(F.relu(self.conv2(x)))   # 7×7
        x = x.view(x.size(0), -1)
        x = F.relu(self.fc1(x))               # dropout removed here
        return self.fc2(x)


# ─────────────────────────────────────────────────────────────────────────
#  Small ResNet-style CNN for CIFAR-10
# ─────────────────────────────────────────────────────────────────────────
class ResBlock(nn.Module):
    def __init__(self, in_ch, out_ch, stride=1):
        super().__init__()
        self.conv1 = nn.Conv2d(in_ch, out_ch, 3, stride, 1, bias=False)
        self.bn1   = nn.BatchNorm2d(out_ch)
        self.conv2 = nn.Conv2d(out_ch, out_ch, 3, 1, 1, bias=False)
        self.bn2   = nn.BatchNorm2d(out_ch)
        self.skip  = nn.Sequential()
        if stride != 1 or in_ch != out_ch:
            self.skip = nn.Sequential(
                nn.Conv2d(in_ch, out_ch, 1, stride, bias=False),
                nn.BatchNorm2d(out_ch))
    def forward(self, x):
        return F.relu(self.bn2(self.conv2(F.relu(self.bn1(self.conv1(x))))) + self.skip(x))

class CifarCNN(nn.Module):
    def __init__(self, num_classes=10):
        super().__init__()
        self.prep   = nn.Sequential(nn.Conv2d(3,64,3,1,1,bias=False), nn.BatchNorm2d(64), nn.ReLU())
        self.layer1 = ResBlock(64,  128, stride=2)
        self.layer2 = ResBlock(128, 256, stride=2)
        self.layer3 = ResBlock(256, 512, stride=2)
        self.pool   = nn.AdaptiveAvgPool2d(1)
        self.fc     = nn.Linear(512, num_classes)
    def forward(self, x):
        x = self.layer3(self.layer2(self.layer1(self.prep(x))))
        return self.fc(self.pool(x).view(x.size(0), -1))


def get_model(dataset_name: str) -> nn.Module:
    name = dataset_name.lower()
    if   name in ('mnist', 'fmnist'): return MnistCNN(10)
    elif name == 'cifar10':           return CifarCNN(10)
    else: raise ValueError(f'No model for {name}')

def get_params(model):   return [v.cpu().numpy() for _, v in model.state_dict().items()]
def set_params(model, params):
    sd = OrderedDict({k: torch.tensor(v) for k, v in zip(model.state_dict().keys(), params)})
    model.load_state_dict(sd, strict=True)

def model_size_mb(model): return sum(p.numel() for p in model.parameters()) * 4 / 1024**2

