"""## 🤝 Cell 5 — Flower Client (OortClient)"""

class OortClient(fl.client.NumPyClient):
    """
    Flower NumPyClient that:
      1. Trains locally with SGD (per course guidelines)
      2. Reports training loss back → used by OortStrategy for utility scoring
      3. Simulates system heterogeneity via compute_speed
    """

    def __init__(self, cid: int, train_indices: List[int],
                 train_dataset, test_dataset,
                 dataset_name: str, compute_speed: float = 1.0):
        self.cid           = cid
        self.train_indices = train_indices
        self.train_dataset = train_dataset
        self.test_dataset  = test_dataset
        self.dataset_name  = dataset_name
        self.compute_speed = compute_speed
        self.model         = get_model(dataset_name).to(DEVICE)

    def get_parameters(self, config):
        return get_params(self.model)

    def fit(self, parameters, config):
        set_params(self.model, parameters)

        # ✅ FIX 3a: Skip training entirely if client has no data
        # (can happen with extreme Dirichlet alpha + many clients)
        if len(self.train_indices) == 0:
            return get_params(self.model), 0, {
                'train_loss':    0.0,
                'train_samples': 0,
                'compute_speed': self.compute_speed,
                'client_id':     self.cid,
            }

        self.model.train()

        epochs     = int(config.get('local_epochs', 5))
        batch_size = int(config.get('batch_size',   32))
        lr         = float(config.get('lr',          0.01))

        # ✅ FIX 3b: Clamp batch_size so it never exceeds available samples
        # Prevents DataLoader RandomSampler from crashing on tiny partitions
        batch_size = min(batch_size, len(self.train_indices))

        loader    = DataLoader(Subset(self.train_dataset, self.train_indices),
                               batch_size=batch_size, shuffle=True)
        optimizer = torch.optim.SGD(self.model.parameters(), lr=lr, momentum=0.9)
        criterion = nn.CrossEntropyLoss()

        last_loss = 0.0
        for epoch in range(epochs):
            running_loss, n = 0.0, 0
            for x, y in loader:
                x, y = x.to(DEVICE), y.to(DEVICE)
                optimizer.zero_grad()
                loss = criterion(self.model(x), y)
                loss.backward()
                optimizer.step()
                running_loss += loss.item() * len(y)
                n += len(y)
            last_loss = running_loss / max(n, 1)

        return get_params(self.model), len(self.train_indices), {
            # Oort needs these metrics for utility scoring
            'train_loss':    float(last_loss),
            'train_samples': len(self.train_indices),
            'compute_speed': self.compute_speed,
            'client_id':     self.cid,
        }

    def evaluate(self, parameters, config):
        set_params(self.model, parameters)
        self.model.eval()
        loader    = DataLoader(self.test_dataset, batch_size=256, shuffle=False)
        criterion = nn.CrossEntropyLoss()
        loss, correct, total = 0.0, 0, 0
        with torch.no_grad():
            for x, y in loader:
                x, y = x.to(DEVICE), y.to(DEVICE)
                out   = self.model(x)
                loss += criterion(out, y).item() * len(y)
                correct += (out.argmax(1) == y).sum().item()
                total   += len(y)
        return float(loss/max(total,1)), total, {'accuracy': float(correct/max(total,1))}

